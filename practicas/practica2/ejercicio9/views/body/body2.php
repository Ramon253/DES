        <h2>Gameplay</h2>
        <p><strong>Estilo de juego</strong>: CS:GO es conocido por su enfoque en el combate táctico y la precisión en los disparos. Es un
            juego basado en rondas donde dos equipos, terroristas y antiterroristas, compiten para completar objetivos
            específicos. <br><br>
            <strong>Compras de armas:</strong> Al comienzo de cada ronda, los jugadores deben comprar armas y equipo con el dinero ganado
            en rondas anteriores. La gestión del dinero es crucial para el éxito del equipo.
            <br><br>
            <strong>Economía:</strong> La economía es un elemento esencial, ya que los jugadores deben decidir cuándo gastar su dinero en
            armas y cuándo ahorrar para rondas futuras.
        </p>
