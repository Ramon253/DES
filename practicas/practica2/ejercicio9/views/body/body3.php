        <h2>Gameplay</h2>
        <p><strong>Estilo de juego:</strong> Valorant combina elementos de CS:GO con habilidades de personajes únicas. Dos equipos se
            enfrentan, y el juego se centra en la plantación/desactivación de bombas o rescate/secuestro de rehenes.
            <br><br>
            <strong>Agentes y habilidades:</strong> Cada jugador elige un agente con habilidades únicas. Estas habilidades pueden variar
            desde curación hasta dispositivos de bloqueo de visión. Las habilidades a menudo se utilizan
            estratégicamente para ganar ventaja.
            <br><br>
            <strong>Compra de armas:</strong> Similar a CS:GO, los jugadores compran armas al comienzo de cada ronda, pero en Valorant,
            también pueden usar puntos obtenidos durante la ronda para mejorar sus habilidades.
        </p>
