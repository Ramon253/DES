        <h2>Gameplay</h2>
        <p>
            <strong>Estilo de juego:</strong> Siege se centra en el combate táctico y la destrucción del entorno. Dos equipos, los
            atacantes y los defensores, compiten en rondas para asegurar o defender un objetivo.
            <br><br>
            <strong>Operadores y habilidades:</strong> Cada jugador elige un operador con habilidades únicas. Estas habilidades pueden
            variar desde dispositivos de observación hasta herramientas de demolición. La elección de operadores y la
            coordinación del equipo son cruciales.
            <br><br>
            <strong>Destrucción del entorno:</strong> Siege permite una destrucción significativa del entorno, lo que significa que los
            jugadores pueden crear agujeros en las paredes y pisos para sorprender a los oponentes.
        </p>
